package com.common;

import java.util.Date;

public class User {

	private int cardID; //定义借阅证号

	private String name; //定义姓名

	private String sex; //定义书的作者

	private String typeOfCard; //定义书的出版社

	private Date per_of_val; //定义书的入库日期

	private double money_Reserved; //定义书的价格

	/**
	 * 定义setTer与getTer方法
	 */
	public void setCardID(int cardID){
		this.cardID = cardID;
	}

	public int getCardID(){
		return cardID;
	}

	public void setName(String name){
		this.name = name;
	}

	public String getName(){
		return name;
	}

	public void setSex(String sex){
		this.sex = sex;
	}

	public String getSex(){
		return sex;
	}

	public void setTypeOfCard(String typeOfCard){
		this.typeOfCard = typeOfCard;
	}

	public String getTypeOfCard(){
		return typeOfCard;
	}

	public void setPer_of_val(Date per_of_val){
		this.per_of_val = per_of_val;
	}

	public Date getPer_of_val(){
		return per_of_val;
	}

	public void setMoney_Reserved(double money_Reserved){
		this.money_Reserved = money_Reserved;
	}

	public double getMoney_Reserved(){
		return money_Reserved;
	}




}
